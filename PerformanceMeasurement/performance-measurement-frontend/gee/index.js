const displayResults = (data) => {
  const resultsContainer = document.getElementById("result");
  let htmlContent = "";

  // Loop through the object and display each benchmark result
  for (const [key, value] of Object.entries(data)) {
    htmlContent += `<h2>${key}</h2>`;
    htmlContent += "<table>";
    htmlContent +=
      "<tr><th>Benchmark</th><th>Score</th><th>Score Error</th><th>Sample Count</th><th>Min</th><th>Max</th><th>Standard Deviation</th></tr>";

    for (const [benchmark, benchmarkData] of Object.entries(value.results)) {
      htmlContent += `
                <tr>
                    <td>${benchmark}</td>
                    <td>${benchmarkData.score}</td>
                    <td>${benchmarkData.scoreError}</td>
                    <td>${benchmarkData.sampleCount}</td>
                    <td>${benchmarkData.min}</td>
                    <td>${benchmarkData.max}</td>
                    <td>${benchmarkData.standardDeviation}</td>
                </tr>
            `;
    }

    htmlContent += "</table>";
  }

  resultsContainer.innerHTML = htmlContent;
};รง
